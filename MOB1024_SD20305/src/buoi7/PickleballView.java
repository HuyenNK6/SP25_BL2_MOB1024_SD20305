    private void loadDataTable() {
        defaultTableModel
                = (DefaultTableModel) tblPickleball.getModel();
        defaultTableModel.setRowCount(0);
        for (Pickleball pickleball : list) {
            defaultTableModel.addRow(new Object[]{
                pickleball.getMa(),
                pickleball.getTen(),
                pickleball.getThuongHieu(),
                pickleball.getGia(),
                pickleball.getSoLuong(),
                pickleball.getTrangThai() == 1 ? "Mở bán" : "Ngừng bán"
            });
        }
    }

    private void loadComboboxModel() {
        defaultComboBoxModel.addElement("Wilson");
        defaultComboBoxModel.addElement("Joola");
        cboThuongHieu.setModel(defaultComboBoxModel);
    }

    private void fillData(int index) {
        Pickleball pick = list.get(index);//lấy đối tượng tại vị trí index trong list
        txtMa.setText(pick.getMa());
        txtTen.setText(pick.getTen());
        cboThuongHieu.setSelectedItem(pick.getThuongHieu());
        txtGia.setText(String.valueOf(pick.getGia()));
        txtSoLuong.setText(String.valueOf(pick.getSoLuong()));
        if (pick.getTrangThai() == 1) {
            rdoMoBan.setSelected(true);
        } else {
            rdoNgungBan.setSelected(true);
        }
    }

    private Pickleball getValueForm() {
        return new Pickleball(
                txtMa.getText(),
                txtTen.getText(),
                String.valueOf(cboThuongHieu.getSelectedItem()),
                Double.parseDouble(txtGia.getText()),
                Integer.parseInt(txtSoLuong.getText()),
                rdoMoBan.isSelected() ? 1 : 0);
    }


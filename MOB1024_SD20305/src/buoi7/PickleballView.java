    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        //confirm tự làm
        Pickleball pick = getValueForm();
        service.addPick(pick);
        loadDataTable();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int rowIndex = tblPickleball.getSelectedRow();
        if (rowIndex != -1) {
            service.deletePick(rowIndex);
            loadDataTable();
            //thông báo thành công tự làm
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng muốn xóa!");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int rowIndex = tblPickleball.getSelectedRow();
        if (rowIndex != -1) {
            Pickleball pickNew = getValueForm();//tạo đối tượng mới với thông tin từ form
            service.updatePick(rowIndex, pickNew);//gọi update bên service
            loadDataTable();
            //thông báo thành công tự làm
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng muốn sửa!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

